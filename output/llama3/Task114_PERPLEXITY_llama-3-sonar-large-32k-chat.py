
s1 = "Hello"
s2 = "World"
print("Original Strings:")
print("s1: ", s1)
print("s2: ", s2)

# Copying a string
s3 = s1
print("\nCopied String:")
print("s3: ", s3)

# Concatenating strings
s4 = s1 + " " + s2
print("\nConcatenated String:")
print("s4: ", s4)

# Manipulating strings (upper case)
s5 = s1.upper()
print("\nUpper Case String:")
print("s5: ", s5)

# Manipulating strings (lower case)
s6 = s2.lower()
print("\nLower Case String:")
print("s6: ", s6)